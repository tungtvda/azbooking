    <script type='text/javascript'>
        jQuery(function ($) {

            var widget_layers_widget_apptech_new_slider_41_slider = $('#layers-widget-apptech_new_slider-41').swiper({
                mode: 'horizontal'
                , onInit: function (s) {
                    $(document).trigger('layers-slider-init', s);
                }
                , bulletClass: 'swiper-pagination-switch'
                , bulletActiveClass: 'swiper-active-switch swiper-visible-switch'
                , paginationClickable: true
                , watchActiveIndex: true
                , effect: 'fade'

                , pagination: '.widget-layers-widget-apptech_new_slider-41-pages'
                , loop: true
            });

            // Allow keyboard control
            widget_layers_widget_apptech_new_slider_41_slider.enableKeyboardControl();


            $('#layers-widget-apptech_new_slider-41').find('.arrows a').on('click', function (e) {
                e.preventDefault();

                // "Hi Mom"
                $that = $(this);

                if ($that.hasClass('swiper-pagination-switch')) {
                    // Anchors
                    widget_layers_widget_apptech_new_slider_41_slider.slideTo($that.index());
                } else if ($that.hasClass('l-left-arrow')) {
                    // Previous
                    widget_layers_widget_apptech_new_slider_41_slider.slidePrev();
                } else if ($that.hasClass('l-right-arrow')) {
                    // Next
                    widget_layers_widget_apptech_new_slider_41_slider.slideNext();
                }

                return false;
            });

            widget_layers_widget_apptech_new_slider_41_slider.init();

            // Do stuff if this is the first widget.
            if (!$('#layers-widget-apptech_new_slider-41').prev('.widget').length) {
                if (!$('#layers-widget-apptech_new_slider-41').hasClass('.full-screen')) {
                    jQuery('.header-site.header-overlay').css('transition', '0s');
                    setTimeout(function () {
                        jQuery('.header-site.header-overlay').css('transition', '');
                    }, 1000);
                    jQuery('body').addClass('header-overlay-no-push');
                }
            }

            // Fade-in slider after it's been initilaized (FOUC).
            $('#layers-widget-apptech_new_slider-41').removeClass('loading').addClass('loaded');
        });
    </script>

